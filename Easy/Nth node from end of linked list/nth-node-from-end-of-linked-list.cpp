//Function to find the data of nth node from the end of a linked list.
class Solution{
public:
    int getNthFromLast(Node *head, int n)
    {
            Node* temp=head;
           int c=0;
           while(temp)
           {
               c=c+1;
               temp=temp->next;
           }
           int p=c-n+1;
           Node* temp1=head;
           int s=0;
           while(temp1)
           
           {
               s=s+1;
               if(p==s)
               {
                   return temp1->data;
               }
               temp1=temp1->next;
           }
           return -1;
                    
           
    }
};



